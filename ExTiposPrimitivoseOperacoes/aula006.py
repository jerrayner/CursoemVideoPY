# Somando valores

num1 = int(input('Digite um número: ')) # sempre que for fazer alguma operação com números, coloca-se o tipo primitivo do número antes do input, assim como para valores booleanos, números com pontos flutuantes. O valor recebido pelo input sempre será uma string.
num2 = int(input('Digite outro: '))
som = int(num1 + num2)
print(f'A soma entre {num1} e {num2} vale {som}') # para facilitar a digitação e retornar as variaveis em um print no console, apenas coloca 'f' antes do que será exibido no input e chama as variaveis dentro de chaves no print.

