# Dobro, triplo e raiz quadrada

n1 = int(input('Digite um número: '))
n2 = int(n1*2)
n3 = int(n1*3)
n4 = int(n1**(1/2))
print(f'O dobro de {n1} é igual a {n2}.')
print(f'O triplo de {n1} é igual a {n3}.')
print(f'A raiz quadrada de {n1} é igual a {n4}.')

